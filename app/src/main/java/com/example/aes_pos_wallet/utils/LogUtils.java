package com.example.aes_pos_wallet.utils;

import android.util.Log;

public class LogUtils {

    public static void e(String tag, String value) {
        Log.e(tag, value);
    }

    public static void i(String tag, String value) {
        Log.i(tag, value);
    }

    public static void d(String tag, String value) {
        Log.d(tag, value);
    }

    public static void v(String tag, String value) {
        Log.v(tag, value);
    }

    public static void w(String tag, String value) {
        Log.w(tag, value);
    }

    public static void e(String tag, String value, Throwable tr) {
        Log.e(tag, value, tr);
    }

    public static void i(String tag, String value, Throwable tr) {
        Log.i(tag, value, tr);
    }

    public static void d(String tag, String value, Throwable tr) {
        Log.d(tag, value, tr);
    }

    public static void v(String tag, String value, Throwable tr) {
        Log.v(tag, value, tr);
    }

    public static void w(String tag, String value, Throwable tr) {
        Log.w(tag, value, tr);
    }
}
